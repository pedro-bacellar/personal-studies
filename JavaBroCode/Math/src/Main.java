//      9. math

public class Main {
    public static void main(String[] args){

        double result;

        result = Math.PI;               // constante PI
        result = Math.E;                // constante exponencial
        result = Math.pow(2, 4);        // potenciacao
        result = Math.abs(-5);          // modulo
        result = Math.sqrt(9);          // raiz quadrada
        result = Math.round(3.14);      // arredondar inteiro
        result = Math.ceil(3.14);       // arredondar maior inteiro
        result = Math.floor(3.99);      // arredondar menor inteiro
        result = Math.max(10, 20);      // maior valor
        result = Math.min(10, 20);      // menor valor

        System.out.println(result);
    }
}